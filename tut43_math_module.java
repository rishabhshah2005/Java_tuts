public class tut43_math_module {
    public static void main(String[] args) {
        System.out.println("minimum out of 2 and 5: "+Math.min(2, 5));
        System.out.println("Power of 5 to 23: "+Math.pow(5, 23));
        System.out.println("Absolute value: "+Math.abs(24/5));
        System.out.println("Ceil(2.5): "+Math.ceil(2.5));
        System.out.println("Floor(2.5): "+Math.floor(2.5));
        System.out.println("cos1: "+Math.cos(1));
        System.out.println("cos1: "+Math.sin(0)); // angle in radians
    }
}