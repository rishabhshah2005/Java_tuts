public class tut06_increment {
    public static void main(String[] args) {
        int i = 5;
        System.out.println(i++);
        System.out.println(++i);
        System.out.println(i++);
        System.out.println(i);
        System.out.println(i--);
        System.out.println(i);

        while (i<100) {
            System.out.println(i++);
        }
    }
    
    }

